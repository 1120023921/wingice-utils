package com.wingice.utils.period.model;

/**
 * The Enum Recurrence Range Type.
*/
public enum RecurrenceRangeType
{
    /**
    * end Date
    */
    END_DATE,
    /**
    * no End
    */
    NO_END,
    /**
    * numbered
    */
    NUMBERED,
    /**
    * For RecurrenceRangeType values that were not expected from the service
    */
    UNEXPECTED_VALUE
}